public class Main {
    public static void main(String[] args) {
        // Example of basic Java code
        System.out.println("Hello, World!");
        
        // Example of accessing arrays and variables
        int[] array = {1, 2, 3};
        double sum = 0.0;
        for (int i : array) {
            sum += i;
        }
        System.out.println("Sum: " + sum);
    }
}
